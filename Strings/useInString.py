# Assigning a string value "Syphar" to the variable `name`
name = "Syphar"

# Checking if the substring "Sy" is present in the variable `name`
if "Sy" in name:  
    # If "Sy" is found in `name`, this message will be printed
    print("Yes, It's Syphar")
else:  
    # If "Sy" is not found in `name`, this message will be printed
    print("No, It's not Syphar")
