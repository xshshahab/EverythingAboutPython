# Example 1: Storing Information About a Person

# A dictionary is like a collection of key-value pairs, where the key is a unique identifier, and the value is the data you want to store.

# Dictionary to store a person's information
person = {
    "name": "Alice",
    "age": 25,
    "city": "New York"
}

# Accessing values in the dictionary
print(person["name"])  # Output: Alice
print(person["age"])   # Output: 25


# Explanation:

# The keys are "name", "age", and "city".
# The values are "Alice", 25, and "New York".
# You can access values using their keys.