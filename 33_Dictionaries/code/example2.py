# Example 2: Counting Fruits

# You can use dictionaries to store counts or group related data.

# Dictionary to count fruits
fruit_count = {
    "apple": 3,
    "banana": 5,
    "orange": 2
}

# Accessing values and adding new data
print(fruit_count["apple"])  # Output: 3
fruit_count["grape"] = 10    # Add a new key-value pair
print(fruit_count)           # Output: {'apple': 3, 'banana': 5, 'orange': 2, 'grape': 10}


# Explanation:

# The keys are "apple", "banana", "orange", and "grape".
# The values represent the count of each fruit.