# Define a list of strings
sortList = ["Vivek", "Ranjan", "Mohan", "Sita"]

# Print the original list of strings
print("Original list of strings:", sortList)

# Sort the list of strings in ascending order (alphabetically)
sortList.sort()

# Print the sorted list of strings
print("Sorted list of strings:", sortList)

