# Define a list of fruits
fruits = ["Orange", "Papaya", "Coconut", "Banana", "Papaya"]

# Print the original list of fruits
print("Original list:", fruits)

# Use the 'index()' method to find the index of the first occurrence of "Papaya"
index_of_papaya = fruits.index("Papaya")

# Print the index of the first occurrence of "Papaya"
print("Index of 'Papaya':", index_of_papaya)